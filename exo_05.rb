# #{ } permet de concaténer un entier dans un string

puts "On va compter le nombre d'heures de travail à THP"
# On va compter le nombre d'heures de travail à THP
puts "Travail : #{10 * 5 * 11}"
# Travail : 550
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"
# En minutes, ca fait 3 300 
puts "Et en secondes ?"
